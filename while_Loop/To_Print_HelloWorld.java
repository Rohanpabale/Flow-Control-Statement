package while_Loop;

//while loop
public class To_Print_HelloWorld {

	public static void main(String[] args) {
		int i = 1; // giving value to the i
		while (i <= 10) { // applying condition
			System.out.println("hello world");
			i++; // using increament operator it will increase the value of i by 1 till the
					// condition is true
		}
	}
}
